// Buatlah kode untuk memilih minuman.
// Minuman yang tersedia adalah kopi dan teh.
// dengan switch case.

var minuman = "kopi";
switch(minuman){
    case 'kopi':
        console.log("Kamu pilih kopi.");
        break;
case 'teh':
    console.log("Kamu pilih teh.")
    break;
}